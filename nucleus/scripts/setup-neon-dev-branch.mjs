import { writeFile } from 'node:fs/promises';
import { EOL } from 'node:os';

const apiKey = required('NEON_API_KEY');
const projectId = required('NEON_PROJECT_ID');
const databaseName = required('NEON_DB_NAME');
const roleName = required('NEON_DB_ROLE');
const parentBranchName = process.env.NEON_PARENT_BRANCH ?? 'main';
const devBranchName = process.env.NEON_DEV_BRANCH_NAME ?? defaultBranchName();
const envFile = process.env.NEON_DEV_DB_ENV_FILE ?? '.env.neon-dev';
const localOverrideEnvFile = process.env.NEON_LOCAL_OVERRIDE_ENV_FILE ?? '.env.local';
const pooled = process.env.NEON_POOLED !== 'false';
const resetBranch = process.argv.includes('--reset') || process.env.NEON_DEV_BRANCH_RESET === '1';

async function main() {
	console.log(`Using Neon project id: ${projectId}`);
	console.log(`Target dev branch: ${devBranchName}`);
	console.log(`Parent branch: ${parentBranchName}`);

	const existingBranch = await findBranchByName(devBranchName);

	if (existingBranch && resetBranch) {
		await neonFetch(`/projects/${projectId}/branches/${existingBranch.id}`, {
			step: `delete branch (${existingBranch.name})`,
			method: 'DELETE'
		});
	}

	const branch = existingBranch && !resetBranch ? existingBranch : await createBranch();
	await ensureReadWriteEndpoint(branch.id);
	const connectionUri = await getConnectionUri(branch.id);

	await writeFile(
		envFile,
		[
			'# Generated by scripts/setup-neon-dev-branch.mjs',
			`# Branch: ${branch.name}`,
			`DATABASE_URL="${connectionUri}"`
		].join(EOL) + EOL,
		'utf8'
	);

	await writeFile(
		localOverrideEnvFile,
		[
			'# Generated by scripts/setup-neon-dev-branch.mjs',
			`# Local app/runtime override for Neon branch: ${branch.name}`,
			`DATABASE_URL="${connectionUri}"`
		].join(EOL) + EOL,
		'utf8'
	);

	console.log(`Neon dev branch ready: ${branch.name}`);
	console.log(`Branch id: ${branch.id}`);
	console.log(`Wrote DATABASE_URL to ${envFile}`);
	console.log(`Wrote local app override to ${localOverrideEnvFile}`);
	console.log('');
	console.log('Next steps:');
	console.log('1. npm run db:push:dev');
	console.log('2. npm run dev will now use the dev branch locally via .env.local');
	console.log('3. Keep using migrations for prod/CI with npm run db:generate and npm run db:migrate');
}

async function createBranch() {
	const parentBranch = await findBranchByName(parentBranchName);
	const payload = {
		branch: {
			name: devBranchName,
			...(parentBranch ? { parent_id: parentBranch.id } : {})
		}
	};

	const response = await neonFetch(`/projects/${projectId}/branches`, {
		step: 'create branch',
		method: 'POST',
		body: JSON.stringify(payload)
	});

	const branch = response.branch ?? response.branches?.[0];

	if (!branch?.id || !branch?.name) {
		throw new Error('Neon create-branch response did not include branch details');
	}

	return branch;
}

async function ensureReadWriteEndpoint(branchId) {
	const response = await neonFetch(`/projects/${projectId}/branches/${branchId}/endpoints`, {
		step: `list endpoints for branch (${branchId})`
	});
	const existingReadWriteEndpoint = response.endpoints?.find((endpoint) => endpoint.type === 'read_write');

	if (existingReadWriteEndpoint) {
		return existingReadWriteEndpoint;
	}

	const created = await neonFetch(`/projects/${projectId}/endpoints`, {
		step: `create read_write endpoint for branch (${branchId})`,
		method: 'POST',
		body: JSON.stringify({
			endpoint: {
				branch_id: branchId,
				type: 'read_write'
			}
		})
	});

	return created.endpoint ?? created.endpoints?.[0] ?? null;
}

async function getConnectionUri(branchId) {
	const search = new URLSearchParams({
		branch_id: branchId,
		database_name: databaseName,
		role_name: roleName,
		pooled: String(pooled)
	});
	const response = await neonFetch(`/projects/${projectId}/connection_uri?${search.toString()}`, {
		step: 'get connection uri'
	});
	const connectionUri = response.uri ?? response.connection_uri;

	if (!connectionUri) {
		throw new Error('Neon connection-uri response did not include a connection string');
	}

	return connectionUri;
}

async function findBranchByName(name) {
	const search = new URLSearchParams({
		search: name,
		limit: '100'
	});
	const response = await neonFetch(`/projects/${projectId}/branches?${search.toString()}`, {
		step: `list branches (search=${name})`
	});
	return response.branches?.find((branch) => branch.name === name) ?? null;
}

async function neonFetch(path, init = {}) {
	const { step = 'unknown step', ...requestInit } = init;
	const url = `https://console.neon.tech/api/v2${path}`;
	const response = await fetch(url, {
		...requestInit,
		headers: {
			Authorization: `Bearer ${apiKey}`,
			'Content-Type': 'application/json',
			...(requestInit.headers ?? {})
		}
	});

	if (!response.ok) {
		const errorText = await response.text();
		throw new Error(
			[
				`Neon API request failed during: ${step}`,
				`Project id: ${projectId}`,
				`URL: ${url}`,
				`Status: ${response.status}`,
				`Response: ${errorText}`
			].join('\n')
		);
	}

	if (response.status === 204) {
		return {};
	}

	return response.json();
}

function defaultBranchName() {
	const user = process.env.USER ?? process.env.USERNAME ?? 'local';
	return `dev-${user.toLowerCase().replace(/[^a-z0-9-]/g, '-')}`;
}

function required(name) {
	const value = process.env[name];

	if (!value) {
		throw new Error(`${name} is required`);
	}

	return value;
}

main().catch((error) => {
	console.error(error instanceof Error ? error.message : error);
	process.exitCode = 1;
});
